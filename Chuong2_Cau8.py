'''Trình bày các loại lỗi khi lập trình và cách bắt lỗi trong Python'''
'''
Có 3 lỗi thường gặp
+Lỗi cú pháp(Syntax Errors) Cách bắt lỗi là khi thực thi câu lệnh còn được gọi là ngoại lệ và được thông báo bởi trình dịch
+Lỗi thực thi(Run time Errors) Cách bắt lỗi trình thông dịch sẽ dừng thông dịch chương trình tại đây và Python in ra các ngoại lệ và thông báo mô tả cho các ngoại lệ đó
+Lỗi ngữ nghĩa(Sematic Errors) Cách bắt lỗi phụ thuộc vào kinh nghiệm của lập trình viên
'''